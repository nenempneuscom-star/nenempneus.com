// Função auxiliar para obter todas as marcas
export function getMarcas(): string[] {
    return VEICULOS_BRASIL.map(v => v.nome).sort()
}

// Função auxiliar para obter modelos de uma marca
export function getModelosByMarca(marca: string): string[] {
    const veiculoMarca = VEICULOS_BRASIL.find(v => v.nome.toLowerCase() === marca.toLowerCase())
    if (!veiculoMarca) return []
    return veiculoMarca.modelos.map(m => m.nome).sort()
}

// Função auxiliar para obter anos de um modelo
export function getAnosByMarcaModelo(marca: string, modelo: string): number[] {
    const veiculoMarca = VEICULOS_BRASIL.find(v => v.nome.toLowerCase() === marca.toLowerCase())
    if (!veiculoMarca) return []

    const veiculoModelo = veiculoMarca.modelos.find(m => m.nome.toLowerCase() === modelo.toLowerCase())
    if (!veiculoModelo) return []

    return veiculoModelo.anosDisponiveis.sort((a, b) => b - a) // Ordenar do mais recente pro mais antigo
}

// Função auxiliar para obter medidas de pneu por marca/modelo/ano
export function getMedidasPneu(marca: string, modelo: string, ano?: number): string[] {
    const veiculoMarca = VEICULOS_BRASIL.find(v => v.nome.toLowerCase() === marca.toLowerCase())
    if (!veiculoMarca) return []

    const veiculoModelo = veiculoMarca.modelos.find(m => m.nome.toLowerCase() === modelo.toLowerCase())
    if (!veiculoModelo) return []

    // Se ano foi especificado, verificar se está disponível
    if (ano && !veiculoModelo.anosDisponiveis.includes(ano)) {
        return []
    }

    return veiculoModelo.medidasPneu
}

// Função para converter medida "175/70/14" em objeto com largura, perfil, aro
export function parseMedida(medida: string): { largura: number; perfil: number; aro: number } | null {
    const partes = medida.split('/')
    if (partes.length !== 3) return null

    const [largura, perfil, aro] = partes.map(Number)
    if (isNaN(largura) || isNaN(perfil) || isNaN(aro)) return null

    return { largura, perfil, aro }
}

// Função para buscar veículo por termo (autocomplete)
export function buscarVeiculos(termo: string): { marca: string; modelo: string }[] {
    const resultados: { marca: string; modelo: string }[] = []
    const termoLower = termo.toLowerCase()

    for (const marca of VEICULOS_BRASIL) {
        for (const modelo of marca.modelos) {
            const nomeCompleto = `${marca.nome} ${modelo.nome}`.toLowerCase()
            if (nomeCompleto.includes(termoLower) ||
                marca.nome.toLowerCase().includes(termoLower) ||
                modelo.nome.toLowerCase().includes(termoLower)) {
                resultados.push({
                    marca: marca.nome,
                    modelo: modelo.nome
                })
            }
        }
    }

    return resultados.slice(0, 20) // Limitar a 20 resultados
}
